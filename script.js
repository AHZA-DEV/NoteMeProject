// Fungsi untuk menampilkan posts
function displayPosts() {
    const postsGrid = document.querySelector('.posts-grid');
    const featuredPosts = document.querySelector('.featured-posts');
    
    if (!postsGrid || !featuredPosts) return;

    postsGrid.innerHTML = '';
    featuredPosts.innerHTML = '';

    staticPosts.forEach((post, index) => {
        const postCard = createPostCard(post);
        
        if (index < 3) {
            // Tambahkan ke featured posts
            featuredPosts.innerHTML += `
                <div class="col-md-4" data-aos="fade-up" data-aos-delay="${index * 100}">
                    <div class="featured-post">
                        ${postCard}
                    </div>
                </div>
            `;
        } else {
            // Tambahkan ke regular posts grid
            postsGrid.innerHTML += `
                <div class="col-md-4 col-lg-3" data-aos="fade-up">
                    ${postCard}
                </div>
            `;
        }
    });
}

// Fungsi untuk membuat kartu post
function createPostCard(post) {
    return `
        <div class="card h-100">
            <img src="${post.image}" class="card-img-top" alt="${post.title}">
            <div class="card-body">
                <div class="d-flex align-items-center mb-2">
                    <img src="${post.author.avatar}" class="rounded-circle me-2" alt="${post.author.name}" width="30" height="30">
                    <span id="text-author" class="text-light small">${post.author.name}</span>
                </div>
                <h5 class="card-title">${post.title}</h5>
                <p class="text-light mb-2">${post.category} · ${post.date} · ${post.readTime}</p>
                <p class="card-text">${post.content.substring(0, 30)}...</p>
                <div class="post-tags mb-3">
                    ${post.tags.map(tag => `<span class="tag">#${tag}</span>`).join('')}
                </div>
                <div class="d-flex justify-content-between align-items-center">
                    <button class="btn btn-outline-primary btn-sm" onclick="viewPost(${post.id})">
                        Read More
                    </button>
                    <div class="d-flex align-items-center">
                        <span class="me-2">
                            <i class="fas fa-eye"></i> ${post.views}
                        </span>
                    </div>
                </div>
            </div>
        </div>
    `;
}

// Fungsi untuk melihat detail post
function viewPost(id) {
    const post = staticPosts.find(p => p.id === id);
    if (!post) return;

    const postDetail = document.querySelector('.post-detail');
    postDetail.innerHTML = `
        <article>
            <div class="text-center mb-4">
                <img src="${post.image}" class="img-fluid rounded" alt="${post.title}" style="max-height: 400px; object-fit: cover;">
            </div>
            <div class="post-meta d-flex align-items-center mb-4">
                <img src="${post.author.avatar}" class="rounded-circle me-2" alt="${post.author.name}" width="50" height="50">
                <div>
                    <h6 class="mb-0">${post.author.name}</h6>
                    <small class="text-muted">${post.date} · ${post.readTime}</small>
                </div>
            </div>
            <h1 class="display-4 mb-3">${post.title}</h1>
            <div class="post-tags mb-4">
                ${post.tags.map(tag => `<span class="tag">#${tag}</span>`).join('')}
            </div>
            <div class="post-content">
                ${formatContent(post.content)}
            </div>
            <div class="mt-4 d-flex align-items-center justify-content-between">
                <div class="d-flex align-items-center">
                    <span class="me-3">
                        <i class="fas fa-eye"></i> ${post.views} views
                    </span>
                    <span>
                        <i class="fas fa-clock"></i> ${post.readTime}
                    </span>
                </div>
            </div>
        </article>
    `;

    const viewPostModal = new bootstrap.Modal(document.getElementById('viewPostModal'));
    viewPostModal.show();
}

// Fungsi untuk memformat konten post
function formatContent(content) {
    return content.split('\n').map(paragraph => {
        paragraph = paragraph.trim();
        if (!paragraph) return '';
        return `<p>${paragraph}</p>`;
    }).join('');
}

// Filter berdasarkan kategori
function filterByCategory(category) {
    const filteredPosts = category === 'all' 
        ? staticPosts 
        : staticPosts.filter(post => post.category.toLowerCase() === category.toLowerCase());
    displayFilteredPosts(filteredPosts);

    // Update tombol kategori aktif
    document.querySelectorAll('.category-pills .btn').forEach(btn => {
        btn.classList.toggle('active', btn.textContent.toLowerCase() === category);
    });
}

// Pencarian posts
function searchPosts() {
    const searchTerm = document.getElementById('searchInput').value.toLowerCase();
    const filteredPosts = staticPosts.filter(post => 
        post.title.toLowerCase().includes(searchTerm) ||
        post.content.toLowerCase().includes(searchTerm) ||
        post.tags.some(tag => tag.toLowerCase().includes(searchTerm))
    );
    displayFilteredPosts(filteredPosts);
}

// Sortir posts
function sortPosts() {
    const sortSelect = document.getElementById('sortSelect');
    const sortedPosts = [...staticPosts];

    switch(sortSelect.value) {
        case 'newest':
            sortedPosts.sort((a, b) => new Date(b.date) - new Date(a.date));
            break;
        case 'oldest':
            sortedPosts.sort((a, b) => new Date(a.date) - new Date(b.date));
            break;
        case 'popular':
            sortedPosts.sort((a, b) => b.views - a.views);
            break;
    }

    displayFilteredPosts(sortedPosts);
}

